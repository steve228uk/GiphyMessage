import Foundation

public struct GiphyImage {
    
    /// Height of the image
    public var height: Int?
    
    /// URL to the MP4 version
    public var mp4: String?
    
    /// Size in bytes of the MP4 version
    public var mp4Size: String?
    
    /// Size in bytes of the GIF
    public var size: String?
    
    /// URL to the GIF
    public var url: String?
    
    /// URL to the WebP verions
    public var webp: String?
    
    /// Size in bytes of the WebP Version
    public var webpSize: String?
    
    /// Width of the image
    public var width: Int?
    
}
